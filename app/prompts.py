# シナリオと難易度ごとのプロンプトを管理するファイル

# 難易度ごとの顧客の態度を定義
DIFFICULTY_SETTINGS = {
    "初級": "あなたは素直で協力的な顧客です。質問はシンプルで、怒ったりせず、敬語で話します。",
    "中級": "あなたは少し感情的な顧客です。要求が複雑で、話が脱線しがちです。",
    "上級": "あなたは強いクレームを持つ顧客です。理不尽な要求をすることがあり、言葉がきつい場面もあります。",
}

# シナリオごとの状況説明を定義
SCENARIO_SETTINGS = {
    "解約引き止め": "あなたは契約中のサービスを解約しようとしています。オペレーターの対応次第では解約を思いとどまる可能性があります。",
    "請求トラブル": "あなたは請求金額に疑問を持っており、その説明を求めています。",
    "初歩的な使い方質問": "あなたはサービスの基本的な使い方がわからず、困っています。",
    "クレーム対応": "あなたはサービスに強い不満を持っており、その不満をオペレーターにぶつけています。",
    "新規契約・CV獲得": "あなたはオンラインショップでの新規契約を検討していますが、料金プランや手続きについて不安や疑問を持っています。",
}

# ロールプレイ用のプロンプトを組み立てる関数
def build_roleplay_prompt(difficulty: str, scenario: str, knowledge: str = "") -> str:
    difficulty_text = DIFFICULTY_SETTINGS[difficulty]
    scenario_text = SCENARIO_SETTINGS[scenario]

    # ナレッジがある場合は追加する
    knowledge_section = ""
    if knowledge:
        knowledge_section = f"""
【参考ナレッジ】
以下はデモSIMのサービス情報です。
顧客としてこの情報を「なんとなく知っている」程度で会話に活かしてください。
{knowledge}
"""

    return f"""
あなたはロールプレイの顧客役です。
以下の設定に従って会話してください。

【あなたの態度】
{difficulty_text}

【状況】
{scenario_text}
{knowledge_section}
【ルール】
・顧客役として自然に会話してください。
・オペレーターの対応に応じて反応を変えてください。
・絶対に顧客役を外れないでください。
・ナレッジの情報は「なんとなく知っている」程度の使い方にしてください。
"""


# フィードバック生成用のプロンプトを組み立てる関数
def build_feedback_prompt(conversation: str) -> str:
    return f"""
以下はオペレーターと顧客のロールプレイの会話履歴です。

【会話履歴】
{conversation}

この会話をもとに、オペレーターの対応を以下の5つの観点で評価してください。

【評価観点】
1. 共感表現：顧客の感情を受け止める言葉があったか
2. 解決速度：適切なタイミングで解決に導けたか
3. 言葉遣い：敬語・丁寧語が適切だったか
4. 問題解決力：顧客の課題を正しく把握・解決できたか
5. 総合スコア：100点満点で採点

【出力形式】
各観点について、スコア（20点満点）とコメントを出力してください。
最後に改善アドバイスを具体的なセリフ例とともに提示してください。
"""